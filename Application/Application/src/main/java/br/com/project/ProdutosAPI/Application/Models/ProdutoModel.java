package br.com.project.ProdutosAPI.Application.Models;

public class ProdutoModel {
    private String title;
    private double price;

    public ProdutoModel(String title, double price) {
        this.title = title;
        this.price = price;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }
}
